package update;

public class Delete {
	protected String what;
	protected String who;


public Delete() {
}

public Delete(String what, String who) {
	this.what = what;
	this.who = who;	
}

public String getWhat() {
	return what;
}

public void setWhat(String what) {
	this.what = what;
}

public String getWho() {
	return who;
}

public void setWho(String who) {
	this.who = who;
}

}